package com.example.flappybird;


public class BitmapElement {
    private int x;
    private int y;

    public BitmapElement() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
